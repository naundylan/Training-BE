var a = 10;


a = a + 20;
console.log(a); // a = a + 20 = 10 + 20 = 30


a = a + 70;
console.log(a); // a = a + 70 = 30 + 70 = 100


a += 100;
console.log(a); // a += 100 <=> a = a + 100 = 100 + 100 = 200


a -= 50;
console.log(a); // a-= 50 <=> a = a - 50 = 200 - 50 = 150


a /= 50;
console.log(a); // a/= 50 <=> a = a / 50 = 150 / 50 = 3


a *= 2;
console.log(a); // a *= 2 <=> a = a * 2 = 3 * 2 = 6
