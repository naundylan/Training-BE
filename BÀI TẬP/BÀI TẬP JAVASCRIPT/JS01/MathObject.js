console.log(Math.round(99.2));
// Trả về 99 vì số thập phân = 2 (nhỏ hơn 5 nên làm tròn xuống)


console.log(Math.round(99.5));
// Trả về 100 vì số thập phân = 5 (lớn hơn hoặc bằng 5 nên làm tròn lên)


console.log(Math.ceil(99.2));
// Trả về 100 vì ceil làm tròn lên


console.log(Math.floor(99.9));
// Trả về 99 vì floor làm tròn xuống


console.log(Math.min(10, 20, 100, -100, 90));
// Trả về -100


console.log(Math.max(10, 20, 100, -100, 90));
// Trả về 100


console.log(Math.pow(2, 4));
// Trả về 2 mũ 4 = 2^4 = 16


console.log(Math.random());
// Trả về 0.46481923092989685 (Một số thập phân ngẫu nhiên)


console.log(Math.trunc(99.5));
// Trả về 99, vì trunc bỏ đi các số sau dấu thập phân
